-(void)createBranch:(CGRect)frame {

    CGPoint p[3] = {
        frame.origin,
        CGPointMake(frame.origin.x,frame.origin.y+frame.size.height),
        CGPointMake(frame.origin.x+frame.size.width, frame.origin.y + frame.size.height),
    };
    
    C4Shape *branch = [C4Shape polygon:p pointCount:3];
    
    CGPoint origin = frame.origin;
    if(frame.size.height < 0) origin.y += frame.size.height;
    
    branch.origin = origin;
    branch.strokeEnd = 0.0f;
    
    [self.branches addObject:branch];

    C4Shape *knot = [C4Shape ellipse:CGRectMake(0, 0, 8, 8)];
    knot.strokeColor = C4GREY;
    knot.fillColor = [UIColor whiteColor];

    CGPoint center = p[0];
    knot.center = center;
    knot.alpha = 0.0f;
    [self.knots addObject:knot];
}

-(void)animateToMidPointAndStop {
    self.sketch.animationDuration = 2.0f;
    self.sketch.strokeEnd = 0.5950;
    [self runMethod:@"switchToSecondSketch" afterDelay:2.0f];
}

-(void)switchToSecondSketch {
    CGPathRef sketchPath2 = [self sketchPath2];
    CGPathRef transformedSketchPath2 = [self scalePath:sketchPath2];
    self.sketch.animationDuration = 0.0f;
    self.sketch.path = transformedSketchPath2;
    CGPathRelease(sketchPath2);
    CGPathRelease(transformedSketchPath2);
    [self runMethod:@"setSecondSketch" afterDelay:0.01f];
    self.sketch.strokeEnd = 0.4250f;
}

-(void)setSecondSketch {
    self.sketch.animationDuration = 0.001f;
    self.sketch.strokeEnd = 0.4250;
    [self runMethod:@"drawSecondSketch" afterDelay:1.0f];
}

-(void)drawSecondSketch {
    self.sketch.animationDuration = 4.0f;
    self.sketch.animationOptions = LINEAR;
    self.sketch.strokeEnd = 1.0f;
}

-(void)createFirstSketch {
    self.sketch = [C4Shape ellipse:CGRectMake(0, 0, 1, 1)];
    CGPathRef sketch1Path = [self sketchPath1];
    CGPathRef transformedSketchPath1 = [self scalePath:sketch1Path];
    self.sketch.path = transformedSketchPath1;
    self.sketch.origin = CGPointMake(640, 115);
    self.sketch.fillColor = [UIColor clearColor];
    self.sketch.lineWidth = 2.0f;
    self.sketch.lineJoin = JOINROUND;
    self.sketch.lineCap = CAPROUND;
    self.sketch.strokeColor = C4GREY;
    self.sketch.strokeEnd = 0.0f;
    CGPathRelease(sketch1Path);
    CGPathRelease(transformedSketchPath1);
}

-(CGPathRef)scalePath:(CGPathRef)path {
    const CGAffineTransform t = CGAffineTransformMakeScale(0.5, 0.5);
    return CGPathCreateCopyByTransformingPath(path, &t);
}

