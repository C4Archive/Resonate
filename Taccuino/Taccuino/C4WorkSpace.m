}

-(void)revealBranch:(NSInteger)index duration:(CGFloat)duration delay:(CGFloat)delay {
    C4Shape *knot = self.knots[index];
    knot.animationDuration = 0.25f;
    knot.animationDelay = delay;
    knot.alpha = 1.0f;
    
    C4Shape *branch = self.branches[index];
    branch.animationDuration = duration;
    branch.animationDelay = delay + 0.25f;
    branch.strokeEnd = 1.0f;
}


